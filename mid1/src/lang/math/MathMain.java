package lang.math;

public class MathMain {
    public static void main(String[] args) {

        System.out.println("Math.ceil(): " + Math.ceil(Math.PI));
        System.out.println("Math.floor(): " + Math.floor(Math.PI));

        System.out.println("Math.random(): " + Math.random()); // 0.0 ~ 1.0
    }
}
